# Stylometric Analysis of German Court Judgments

## Project

This project investigates stylistic differences between civil law and criminal law court judgments in Germany.

The goal is to determine whether stylometric features can be used to automatically distinguish between these two legal domains.

## Research Question

Can linguistic features be used to identify whether a court judgment belongs to civil or criminal law?

## Data

The dataset consists of publicly available German court decisions from OpenLegalData.

https://openlegaldata.io

Due to licensing restrictions the raw data is not included.

## Methods

The analysis uses stylometric methods including:

- word frequency analysis
- sentence length statistics
- principal component analysis (PCA)

All analyses were implemented in R.

## Repository Structure

data/ – dataset description  
scripts/ – R analysis scripts  
results/ – generated plots and outputs  
paper/ – project report  

## Reproducibility

To reproduce the analysis:

1 Download the dataset  
2 Place the data in the /data folder  
3 Run the R scripts in the /scripts folder

